class Post
{
public static void main(String args[])
{
int counter=0;
System.out.println("Counter is:"+counter);
System.out.println("\n++Counter is:"+ ++counter);
System.out.println("\nCounter is:"+counter);
counter=0;
System.out.println("\nCounter is:"+counter);
System.out.println("\nCounter++ is:"+counter++);
System.out.println("\nCounter is:"+counter);
}
}
